// Task 1 : write a functions to check if a number is even or odd and log the result to the console.
function checkNumberEvenOrOdd(number){
    if(number%2==0){
        console.log(`The number is even ${number}`);
    }
    else{
        console.log(`The number is odd ${number}`)
    }
}
checkNumberEvenOrOdd(5)